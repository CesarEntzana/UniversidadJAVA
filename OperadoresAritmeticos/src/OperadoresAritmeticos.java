public class OperadoresAritmeticos {
    

    public static void main(String[] args) {
       // Opradores aritéticos
       int a=10;
       int b=20;

       // Suma 
       var resultado = a+b;
       System.out.println("Suma a + b = "+resultado);
       
       // Resta
       resultado = a-b;
       System.out.println("Resta a - b = "+resultado);
       
       // Multiplicación 
       resultado = a*b;
       System.out.println("Multiplicación a * b = "+resultado);
       
       // División 
       var  div = (float)a/b;
       System.out.println("División  a / b = "+div);
       
       // Módulo
        resultado = ( a % b);
       System.out.println("Módulo a % b = "+resultado);
       
    }
}
